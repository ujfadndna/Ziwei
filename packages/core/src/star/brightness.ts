/**
 * 合并的亮度表。
 */
export const BRIGHTNESS_TABLE: Partial<Record<StarName, readonly Brightness[]>> = {
  ...MAJOR_STAR_BRIGHTNESS,
  ...MINOR_STAR_BRIGHTNESS,
};

/**
 * 根据星曜和所在宫位地支计算亮度。
 *
 * @param starName 星曜名称
 * @param branch 宫位地支
 * @returns 亮度，如果该星曜没有亮度定义则返回 undefined
 */
export function getBrightness(starName: StarName, branch: EarthlyBranch): Brightness | undefined {
  const brightnessArray = BRIGHTNESS_TABLE[starName];
  if (!brightnessArray) {
    return undefined;
  }

  const branchIndex = getBranchIndex(branch);
  return brightnessArray[branchIndex];
}

/**
 * 判断亮度是否为吉（庙、旺、得）。
 *
 * @param brightness 亮度
 * @returns 是否为吉
 */
export function isAuspiciousBrightness(brightness: Brightness): boolean {
  return brightness === "庙" || brightness === "旺" || brightness === "得";
}

/**
 * 判断亮度是否为凶（不、陷）。
 *
 * @param brightness 亮度
 * @returns 是否为凶
 */
export function isInauspiciousBrightness(brightness: Brightness): boolean {
  return brightness === "不" || brightness === "陷";
}
