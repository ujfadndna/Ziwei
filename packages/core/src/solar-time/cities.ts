function normalizeQuery(query: string): string {
  return query.trim();
}

export function searchCities(query: string, limit = 10): CityEntry[] {
  const normalized = normalizeQuery(query);
  const normalizedLimit = Number.isFinite(limit) ? Math.max(0, Math.floor(limit)) : 10;
  if (!normalized || normalizedLimit === 0) return [];
  const queryWithoutCitySuffix = normalized.endsWith("市") ? normalized.slice(0, -1) : normalized;
  const ranked: Array<{ entry: CityEntry; score: number; index: number }> = [];

  for (let index = 0; index < CHINA_CITIES.length; index += 1) {
    const entry = CHINA_CITIES[index];
    if (!entry) continue;
    const city = entry.name;
    const province = entry.province;
    const fullName = `${province}${city}`;
    const fullNameWithSuffix = `${province}${city}市`;
    const cityWithSuffix = `${city}市`;

    let score = Number.POSITIVE_INFINITY;
    if (city === normalized || city === queryWithoutCitySuffix || cityWithSuffix === normalized) {
      score = 0;
    } else if (fullName === normalized || fullNameWithSuffix === normalized) {
      score = 1;
    } else if (city.startsWith(queryWithoutCitySuffix)) {
      score = 2;
    } else if (city.includes(queryWithoutCitySuffix)) {
      score = 3;
    } else if (province === normalized || province.startsWith(queryWithoutCitySuffix)) {
      score = 4;
    } else if (fullName.includes(queryWithoutCitySuffix) || fullNameWithSuffix.includes(normalized)) {
      score = 5;
    }

    if (Number.isFinite(score)) {
      ranked.push({ entry, score, index });
    }
  }

  ranked.sort((a, b) => a.score - b.score || a.index - b.index);
  return ranked.slice(0, normalizedLimit).map((item) => item.entry);
}

export function getCitiesByProvince(): Map<string, CityEntry[]> {
  const grouped = new Map<string, CityEntry[]>();
  for (const city of CHINA_CITIES) {
    const current = grouped.get(city.province);
    if (current) current.push(city);
    else grouped.set(city.province, [city]);
  }
  return grouped;
}
